package com.ericsson.bos.so.security.mtls.config;

import lombok.Data;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

import java.util.List;

@Data
@Component
@ConfigurationProperties(prefix = "security.truststore")
@ConditionalOnProperty(value = "security.tls.enabled", havingValue = "true")
public class CaCertSecrets {
    @Value("${security.truststore.path}")
    private String path;

    @Value("${security.truststore.caCertDirectory}")
    private String caCertDirectory;

    private List<Certificates> certificates;
}